/**
 * This file contains primitive sized types.
 */
#ifndef PYR_TYPES_H
#define PYR_TYPES_H


#include <prtypes.h>


namespace pyr {

    // in case we decouple from SDL

    typedef PRUint8  u8;
    typedef PRUint16 u16;
    typedef PRUint32 u32;
    typedef PRUint64 u64;
    
    typedef PRInt8  s8;
    typedef PRInt16 s16;
    typedef PRInt32 s32;
    typedef PRInt64 s64;

}


#endif
